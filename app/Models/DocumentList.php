    */

    public function documentFolders(){
        return $this->belongsToMany('App\Models\DocumentFolder', 'list_id', 'id');
    }

    public function documentFolder(){
        return $this->belongsTo('App\Models\DocumentFolder', 'list_id', 'id');
    }

    public function documents()
    {
        return $this->belongsToMany('App\Models\Document', 'document_id', 'id');
    }

